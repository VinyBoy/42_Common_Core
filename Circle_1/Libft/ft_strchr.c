#include "libft.h"

char	*ft_strchr(const char *str, int searchedchar)
{
	char	*s;

	s = (char *)str;
	while (*s != (char)searchedchar)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return (s);
}
